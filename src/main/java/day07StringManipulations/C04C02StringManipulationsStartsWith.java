package day07StringManipulations;

public class C04C02StringManipulationsStartsWith {
    public static void main(String[] args) {
        /*
 * startsWith()
 *  Metnin bas kisminin istenilen dizi ile eslesip eslesmedigini kontrol eder. Sonucu boolean dir.
 *
 * endsWith()
 * Metnin son kisminin istenilen dizi ile eslesip eslesmedigini kontrol eder. Sonucu boolean dir.   */


        String cumle ="Kazananlar hic hata yapmayanlar degil, vazgecmeyenledir";
        System.out.println(cumle.startsWith("Ka")); //Ka ile basliyor mu?
        System.out.println(cumle.endsWith("dir"));  //dir ile bitiyor mu?






    }
}
